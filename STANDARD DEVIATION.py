# VARIANCE : IT MEANS HOW THE VALUES ARE SPREAD .

# STANDARD DEVIATION IS THE SQUARE ROOT OF VARIANCE .

# MATHEMATICAL FORMULA : VARIANCE = STANDARD DEVIATION * STANDARD DEVIATION .

'''
CALCUALTE THE VARIANCE OF THE LIST ARRAY 
[MATHEMATICALLY .

STEPS INVOLVED IN THE CALCULATION OF VARIANCE MATHEMATICALLY .

STEP 1 : FIND THE MEAN OF ALL VALUES .

( 74 + 68 + 72 + 80 + 64 + 70 + 78 + 62 + 76 + 66 ) / 10
= 710 / 10
= 71

STEP 2 : FIND THE DIFFERENCE FROM THE MEAN FOR EACH VALUE .

74 - 71 = +3
68 - 71 = -3
72 - 71 = +1
80 - 71 = +9
64 - 71 = -7
70 - 71 = -1
78 - 71 = +7
62 - 71 = -9
76 - 71 = +5
66 - 71 = -5

STEP 3 : FIND THE SQUARE VALUE FOR EACH DIFFERENCE .

+3 ^ 2 = 9.00
-3 ^ 2 = 9.00
+1 ^ 2 = 1.00
+9 ^ 2 = 81.00
-7 ^ 2 = 49.00
-1 ^ 2 = 1.00
+7 ^ 2 = 49.00
-9 ^ 2 = 81.00
+5 ^ 2 = 25.00
-5 ^ 2 = 25.00

STEP 4 : FIND THE AVERAGE OF ALL SQUARED VALUES .

( 9.00 + 9.00 + 1.00 + 81.00 + 49.00 + 1.00 + 49.00 + 81.00 + 25.00 + 25.00 ) / 10
= 330 / 10
= 33

SO THE VARIANCE IS 33.00

STEP 5 : CALCULATE THE SQUARE ROOT OF THE VARIANCE .

33 ^ 0.5 = 5.744563

SO THE STANDARD DEVIATION IS 5.744563
'''

# CALCUALTE THE VARIANCE OF THE LIST .

import numpy as np
hindi_marks = [74,68,72,80,64,70,78,62,76,66]
variance = np.var(hindi_marks)
print ( "THE VARIANCE IS" , variance )

