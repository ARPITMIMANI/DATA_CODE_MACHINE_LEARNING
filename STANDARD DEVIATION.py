# STANDARD DEVIATION : IT MEANS HOW THE VALUES ARE SPREAD .

# THERE ARE 2 TYPES OF STANDARD DEVIATION : LOW STANDARD DEVIATION AND HIGH STANDARD DEVIVATION .

# EXAMPLE OF LOW STANDARD DEVIATION .
# CALCUALTE THE STANDARD DEVIATION OF THE LIST .

import numpy as np
science_marks = [63,66,64,69,62,68,70,61,67,65]
std_dev = np.std(science_marks)
print ( "THE STANDARD DEVIATION IS" , std_dev )

# EXAMPLE OF HIGH STANDARD DEVIATION .
